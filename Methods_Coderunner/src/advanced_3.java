
public class advanced_3 {

	public static void main(String[] args) 
	{
//		Create a method called formatSentence. 
//		The method has two parameters: name and height. 
//		For example, if the method gets arguments “Justin” and 180, the method prints
//		Justin is 180 cm tall.

		
		formatSentence("Justin", 180);
		
	}
	public static void formatSentence(String name, int height)
	{
		System.out.println(name + " is " + height + " cm tall.");
		
	}
	
	
	
}
